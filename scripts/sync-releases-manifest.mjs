#!/usr/bin/env node
import { copyFileSync, existsSync, mkdirSync, writeFileSync } from "node:fs";
import { spawnSync } from "node:child_process";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const manifestPath = join(root, "src", "data", "releases-manifest.json");
const releaseDataDir = join(root, "src", "data", "releases");

function releaseKey(release) {
  return `${release.platform ?? "srsim"}:${release.id}`;
}

function bundleStem(release) {
  return `${release.platform ?? "srsim"}-${release.id}`;
}

function defaultSchemaFilename(release) {
  return (release.platform ?? "srsim") === "sros"
    ? "sros-supported-hardware.json"
    : "srsim-supported-hardware.json";
}

function listReleasesJson() {
  const candidates = [
    ["uv", ["run", "srsim-hw-schema", "list-releases", "--format", "json"]],
    ["python3", [join(root, "srsim_hw_schema.py"), "list-releases", "--format", "json"]],
    ["python", [join(root, "srsim_hw_schema.py"), "list-releases", "--format", "json"]],
  ];
  for (const [command, args] of candidates) {
    const result = spawnSync(command, args, {
      cwd: root,
      encoding: "utf-8",
      stdio: ["ignore", "pipe", "pipe"],
    });
    if (result.status === 0 && result.stdout.trim()) {
      return JSON.parse(result.stdout);
    }
  }
  throw new Error("failed to run list-releases; install uv or use python with PyYAML available");
}

const manifest = listReleasesJson();
mkdirSync(releaseDataDir, { recursive: true });

for (const release of manifest.releases) {
  release.key = releaseKey(release);
  const source = release.schema_output
    ? join(root, release.schema_output)
    : join(root, "releases", release.platform ?? "srsim", release.id, defaultSchemaFilename(release));
  if (!existsSync(source)) {
    throw new Error(`missing release schema for ${release.key}: ${source}`);
  }
  const stem = bundleStem(release);
  const target = join(releaseDataDir, `${stem}.json`);
  copyFileSync(source, target);
  release.schema_bundle = `releases/${stem}.json`;
}

writeFileSync(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`, "utf-8");
console.log(`wrote ${manifestPath}`);

const bundleLines = [
  "// This file is generated by npm run sync:releases. Do not edit by hand.",
  'import type { HardwareSchema } from "./types";',
  "",
];
for (const release of manifest.releases) {
  bundleLines.push(`import release_${bundleStem(release).replace(/[^a-zA-Z0-9_]/g, "_")} from "./data/releases/${bundleStem(release)}.json";`);
}
bundleLines.push("", "export const bundledReleaseSchemas: Record<string, HardwareSchema> = {");
for (const release of manifest.releases) {
  bundleLines.push(`  "${release.key}": release_${bundleStem(release).replace(/[^a-zA-Z0-9_]/g, "_")} as HardwareSchema,`);
}
bundleLines.push("};", "");

const bundlePath = join(root, "src", "releaseSchemas.ts");
writeFileSync(bundlePath, `${bundleLines.join("\n")}\n`, "utf-8");
console.log(`wrote ${bundlePath}`);

for (const release of manifest.releases) {
  console.log(`copied ${release.key} -> src/data/releases/${bundleStem(release)}.json`);
}
